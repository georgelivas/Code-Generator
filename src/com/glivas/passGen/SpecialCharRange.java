package com.glivas.passGen;

public class SpecialCharRange {
    private int first;
    private int length;

    public SpecialCharRange(int first, int length) {
        this.first = first;
        this.length = length;
    }

    public int getFirst() {
        return this.first;
    }
    public int getLength() {
        return this.length;
    }
}
